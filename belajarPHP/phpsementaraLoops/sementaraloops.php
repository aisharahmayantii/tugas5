<!--Contoh di bawah ini pertama menetapkan $ x variabel untuk 1 ($ x = 1). Kemudian, sementara loop akan terus berjalan selama $ x kurang dari atau sama dengan 5 ($ x <= 5). $ X akan meningkat 1 setiap kali loop berjalan ($ x ++)-->
